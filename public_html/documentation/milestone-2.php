			<p><strong>Landing Page</strong>: Full Screen responsive image with site nav/menu</p>
			<p><strong>About Me</strong>: Quote about ideas, followed by my personal why, followed by my personal and professional strengths and weaknesses. Large blurb on desktop with a full screen responsive background image. On mobile the blurb will display only the quote unless the container is expanded by the user.</p>
			<p><strong>Portfolio</strong>: This will be a slider. A full-size, responsive image as well as a full-size container will give me the flexibility to convey a lot of meaning visually. For example, for my capstone project, I will be able to include icons to demonstrate what software technologies were implemented to complete the project. There will also be a container on the right that can have more thorough documentation. On mobile, this container will appear at the bootom of the screen. I may choose to write a simpler blurb to display on smaller screens, or allow for another expandable container.
			</p>
			<p><strong>Contact</strong>: There will be a live contact form on my site that will send a secure message to my email. This will also display relevant information like my current working location, email and "business" phone. On mobile this extra information will be part of an expandible menu to keep screen clutter down. Also on mobile I would like to include the functionality of a user being able to send an SMS message by clicking the phone number.
			</p>
