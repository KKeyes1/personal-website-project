			<p>Here is a loose structure for the content of my site. This is paired down to the bare-essentials that will be present no matter what. I have tried to design the site so that it can "expand" in areas that may develop more content as the bootcamp progresses, or over time in my career. The slider, for example, should never have more than 5 screens, but it is a dynamic container that I can change out as I see fit to portray new projects or characteristics of my professional persona.</p>
